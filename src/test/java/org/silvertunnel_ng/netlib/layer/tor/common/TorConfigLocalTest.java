	/**
	 * Test method for {@link org.silvertunnel_ng.netlib.layer.tor.common.TorConfig#isCountryAllowed(java.lang.String)}.
	 */
	@Test
	public void testIsCountryAllowed()
	{
		TorConfig.setCountryAllowed("US", false);
		assertTrue(TorConfig.isCountryAllowed("DE"));
		assertTrue(TorConfig.isCountryAllowed("IT"));
		assertTrue(TorConfig.isCountryAllowed("RU"));
		assertFalse(TorConfig.isCountryAllowed("US"));
		TorConfig.setCountryAllowed("DE", false);
		assertFalse(TorConfig.isCountryAllowed("DE"));
		assertTrue(TorConfig.isCountryAllowed("IT"));
		assertTrue(TorConfig.isCountryAllowed("RU"));
		assertFalse(TorConfig.isCountryAllowed("US"));
		TorConfig.setCountryAllowed("US", true);
		assertFalse(TorConfig.isCountryAllowed("DE"));
		assertTrue(TorConfig.isCountryAllowed("IT"));
		assertTrue(TorConfig.isCountryAllowed("RU"));
		assertTrue(TorConfig.isCountryAllowed("US"));
	}

